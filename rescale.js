function Rescale() {
	var canvas = document.getElementById("GameCanvas");
	canvas.width  = window.innerWidth;
	canvas.height = window.innerHeight;
}

function GetCanvasWidth() {
	var canvas = document.getElementById("GameCanvas");
	return canvas.width;
}

function GetCanvasHeight() {
	var canvas = document.getElementById("GameCanvas");
	return canvas.width;
}
